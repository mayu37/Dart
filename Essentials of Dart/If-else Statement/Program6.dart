//Write a dart program, that thake a number from 0 to 5 and print its spelling, if the number is greater
//than 5 print entered number is greater than 5
void main() {
  var x = 4;
  if (x == 1) {
    print("ONE");
  } else if (x == 2) {
    print("TWO");
  } else if (x == 3) {
    print("Three");
  } else if (x == 4) {
    print("Four");
  } else if (x == 5) {
    print("Five");
  } else if (x > 5) {
    print('${x} is greater than 5');
  }
}
